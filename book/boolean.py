is_student = True
has_graduated = False

print(is_student)       # Output: True
print(has_graduated)    # Output: False

# using comparison operator
age = 20
is_adult = age >= 18

print(is_adult)         # Output: True

# bool function
x = 0
flag = bool(x)          # flag will be False because x is 0

y = "hello"
status = bool(y)        # status will be True because y is a non-empty string

print(flag)             # Output: False
print(status)           # Output: True
